# Definimos variables
num1 = input("Introduce un número: ")
num2 = input("Introduce otro número: ")

# Hacemos el proceso if
if num1 > num2:
    print(num1 +" es más grande que "+ num2)
elif num2 > num1:
    print(num2 +" es más grande que "+ num1)
elif num1 == num2:
    print(num2, " es igual que "+ num1)
